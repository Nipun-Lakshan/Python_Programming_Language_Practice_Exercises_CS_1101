# CS 1101 - Week 09 - Video 17 - The range() Function

numbers = range(5)
print(numbers)  # Print the range object

for number in numbers:
    print(number)

numbers = range(5, 10)
for number in numbers:
    print(number)

numbers = range(5, 10, 2)

for number in numbers:
    print(number)

for number in range(5):
    print(number)
