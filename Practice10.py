# CS 1101 - Week 09 - Video 12 - If Statements

temperature = 35
if (temperature > 30):
    print("It's a hot day")
    print("Drink plenty of water")
elif (temperature > 20):
    print("It's a nice day!")
elif (temperature > 10):
    print("It's a bit cold day")
else:
    print("It's cold")
print("Done")
